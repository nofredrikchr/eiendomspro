export function formatKr(value, decimals = 0) {
  if (value === null || value === undefined || isNaN(value)) return '0 kr';
  return (
    Number(value).toLocaleString('nb-NO', {
      minimumFractionDigits: decimals,
      maximumFractionDigits: decimals,
    }) + ' kr'
  );
}

export function formatPct(value, decimals = 2) {
  if (value === null || value === undefined || isNaN(value)) return '0%';
  return Number(value).toFixed(decimals) + '%';
}

export function calcTerminbelop(laan, rente, aar) {
  if (!laan || !rente || !aar) return 0;
  const r = rente / 100 / 12;
  const n = aar * 12;
  if (r === 0) return laan / n;
  return (laan * r * Math.pow(1 + r, n)) / (Math.pow(1 + r, n) - 1);
}

export function calcYield(nettoPerMnd, totalInvestering) {
  if (!totalInvestering || totalInvestering === 0) return 0;
  return ((nettoPerMnd * 12) / totalInvestering) * 100;
}

